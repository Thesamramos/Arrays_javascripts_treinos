//Crie a função imprimirInverso que pega um Array como um argumento e imprime cada elemento em ordem reversa no console.

function imprimirInverso(array){
    return array.reverse()
}

var lista = ['Banana', 'Azul', 'Pokémon', 'Cana de açucar']
console.log(imprimirInverso(lista))

var lista2 = [34, 'Mutante', true]
console.log(imprimirInverso(lista2))